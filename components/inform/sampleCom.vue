<template>
  <div>
    <form method="post" enctype="multipart/form-data">
      <div>
        <label for="chooseFile">
          Click
        </label>
      </div>
      <input ref="image" @change="uploadImg()" type="file" id="chooseFile" name="chooseFile" accept="image/*">
    </form>
    <img  :src="image" alt="" style="width: 500px; height: auto;">
  </div>
</template>

<script>
export default {
  name: "sampleCom",

  data() {
    return {
      image : ''
    }
  },
  methods: {
